INT16U cPLC_postProcess(pvoid h)
{
    P_MSG_INFO  pMsg = (P_MSG_INFO)h;
    INT8U  *pBuf = (UCHAR *)(pMsg->msg_buffer);
    INT16U  mLen = pMsg->msg_header.msg_len;
    OS_CPU_SR  cpu_sr;    


    OS_ENTER_CRITICAL();
    g_msg_len = mLen;
    memcpy(g_msg_buf, pBuf, mLen);

    if((mPLC_REPLY_ENERGY_LEN == mLen) && !(nstrcmp((char *)g_msg_buf, (char *)p_mPLC_REPLY_ENERGY[g_plc_index][g_cur_mplc], mPLC_REPLY_ENERGY_LEN)))
    {
        LED_PLC_TOGGLE();

        g_mplc_state[g_cur_mplc][g_cur_freq] = TRUE;
    }    
    else
    {
        g_mplc_state[g_cur_mplc][g_cur_freq] = FALSE;
    }
    OS_EXIT_CRITICAL();

    OSSemPost(g_sem_plc);

    return (TRUE);
}

INT16U mPLC_postProcess(pvoid h)
{
    P_MSG_INFO  pMsg = (P_MSG_INFO)h;
    INT8U  *pBuf = (UCHAR *)(pMsg->msg_buffer);
    INT16U  mLen = pMsg->msg_header.msg_len;
    OS_CPU_SR  cpu_sr;    


    OS_ENTER_CRITICAL();
    g_msg_len = mLen;
    memcpy(g_msg_buf, pBuf, mLen);

    if((mPLC_READ_ADDR_LEN == mLen) && !(nstrcmp((char *)g_msg_buf, (char *)p_mPLC_READ_ADDR[g_plc_index][g_cur_mplc], mPLC_READ_ADDR_LEN)))
    {
        mplc_reply_addr();
    }

    if((cPLC_READ_ENERGY_LEN == mLen) && !(nstrcmp((char *)g_msg_buf, (char *)p_cPLC_READ_ENERGY[g_plc_index][g_cur_mplc], cPLC_READ_ENERGY_LEN)))
    {
        mplc_reply_energy();
    }    
    OS_EXIT_CRITICAL();

    return (TRUE);
}

