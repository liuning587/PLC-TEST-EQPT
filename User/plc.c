void PLC_Init(void)
{
    INT32U  temp;

    
    PLC_Config();

    mPLC_PWR_ON();
    mPLC_RESET_HIGH();
    mPLC_EVENT_LOW();
    mPLC_SELECT_EN();

    if(TRUE == get_plc_prm(&temp))
    {
        temp &= 0xff;

        if((temp >= PLC_NUM_1) && (temp <= PLC_NUM_8))
        {
            g_plc_num = temp;
        }
        else
        {
            g_plc_num = PLC_NUM_1;
        }
    }
    else
    {
        g_plc_num = PLC_NUM_1;
    }

    g_plc_index = get_plc_index(g_plc_num);

#if 0
    g_plc_index++;
    g_plc_index %= MAX_PLC_NUM;
#endif

    g_cur_mplc = mPLC_NUM_1;
    g_cur_freq = FREQ_270KHz;    
}

INT8U PLC_Checksum(INT8U *ptr, INT16U len)
{
    INT8U  sum;
    INT16U  i;


    sum = 0;
    
    for(i = 0; i < len; i++)
    {
        sum += *ptr;

        ptr++;
    }

    return (sum);
}

void plc_checksum_test(void)
{
    INT8U  i, j, k;


    k = 0;
    
    for(i = 0; i < MAX_PLC_NUM; i++)
    {
        for(j = 0; j < mPLC_NUM; j++)
        {
            g_plc_checksum[k][0] = PLC_Checksum((INT8U *)&((p_cPLC_READ_ENERGY[i][j])[1]), cPLC_READ_ENERGY_LEN - 3);
            g_plc_checksum[k][1] = PLC_Checksum((INT8U *)&((p_mPLC_READ_ADDR[i][j])[1]), mPLC_READ_ADDR_LEN - 3);
            g_plc_checksum[k][2] = PLC_Checksum((INT8U *)&((p_mPLC_REPLY_ADDR[i][j])[1]), mPLC_REPLY_ADDR_LEN - 3);
            g_plc_checksum[k][3] = PLC_Checksum((INT8U *)&((p_mPLC_REPLY_ENERGY[i][j])[0]), mPLC_REPLY_ENERGY_LEN - 2);

            k++;
        }
    }
}

INT16U cplc_read_energy(void)
{
    P_MSG_INFO  pMsg = NULL;
    OS_CPU_SR  cpu_sr;     
    

    if(!(pMsg = (P_MSG_INFO)alloc_send_buffer(MSG_SHORT)))
    {
        return (FALSE);
    }

    OS_ENTER_CRITICAL();
    memcpy(g_cplc_read_energy, p_cPLC_READ_ENERGY[g_plc_index][g_cur_mplc], cPLC_READ_ENERGY_LEN);

    if(FREQ_270KHz == g_cur_freq)
    {
        g_cplc_read_energy[0] = 0xFC;
    }
    else
    {
        g_cplc_read_energy[0] = 0xFA;
    }
    OS_EXIT_CRITICAL();
    
    memcpy(pMsg->msg_buffer, g_cplc_read_energy, sizeof(g_cplc_read_energy));

    pMsg->msg_header.msg_len = sizeof(g_cplc_read_energy);

    pMsg->msg_header.end_id = cPLC_COM_PORT;

    pMsg->msg_header.need_buffer_free = TRUE;
    
    return End_send(pMsg);
}

